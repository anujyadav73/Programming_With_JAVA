package String_tute;

import java.util.*;

public class strings_ba {


    public static void main(String[] args) {
        int n = 5;
        for (int i = 0; i<=n ; i++){
            for (int j = i; j<=n-1 ; j++){
                System.out.print(" " + "*");
            }
            System.out.println(" ");
        }



    }
}
